//Converts Miles to Kilometers

#include <iostream>
using namespace std;

int main ()
{ 
    float km, Miles;
    cout << "Enter km: ";
    cin >> km;
    Miles = km * 0.621371;
    cout << endl << "Miles:" << Miles << "\n\n";
    return 0;
}